const ADVANCE = 5.5; // grid units from one char origin to the next
const GAP = 1.5;     // trailing gap included in ADVANCE

/** Pixel width of a rendered string at the given size. */
export function textWidth(str, size) {
  if (str.length === 0) return 0;
  const unit = size / 6;
  return (str.length * ADVANCE - GAP) * unit;
}

/**
 * Stroke `str` onto the context. `y` is the text baseline (bottom of the
 * glyph cell). The caller configures stroke style/glow; this only draws paths.
 */
export function drawText(ctx, str, x, y, size, align = 'left') {
  const unit = size / 6;
  const upper = String(str).toUpperCase();
  const width = textWidth(upper, size);
  let cx = x;
  if (align === 'center') cx = x - width / 2;
  else if (align === 'right') cx = x - width;
  const top = y - size;

  for (const ch of upper) {
    const glyph = GLYPHS[ch];
    if (glyph) {
      for (const line of glyph) {
        ctx.beginPath();
        ctx.moveTo(cx + line[0][0] * unit, top + line[0][1] * unit);
        for (let i = 1; i < line.length; i++) {
          ctx.lineTo(cx + line[i][0] * unit, top + line[i][1] * unit);
        }
        ctx.stroke();
      }
    }
    cx += ADVANCE * unit;
  }
}
